
<template>
 <div>

    <h1 class="centralizado">{{ titulo }}</h1>
    <p v-show="mensagem" class="centralizado"> {{mensagem}} </p>
    <input type="search" class="filtro" @input="filtro = $event.target.value" placeholder="filtrar por parte do titulo"/>
    <ul class="lista-fotos">
      <li class="lista-fotos-item" v-for="foto in fotosComFiltro">
        <meu-painel :titulo="foto.titulo">
          <imagem v-meu-transform:scale.animate="90" :titulo="foto.titulo" :url="foto.url"/>
          <meu-botao tipo="button" rotulo="REMOVER" :confirmacao="true" estilo="perigo" @botaoAtivado="remove(foto)"/>
          <router-link :to='{name:"altera",params:{id:foto._id}}'><meu-botao tipo="button" rotulo="Alterar" :confirmacao="false"/></router-link>
        </meu-painel>
      </li>
    </ul>

  </div>
</template>

<script>
import Painel from '../shared/painel/Painel.vue'
import imagemResponsiva from '../shared/image-responsiva/imagemResponsiva.vue'
import Botao from '../shared/botao/Botao.vue';
import FotoService from '../../domain/foto/FotoService';
export default {
  components:{
    'meu-painel':Painel,
    'imagem':imagemResponsiva,
    'meu-botao':Botao
  },
  data(){
    return {
      titulo:"AluraPic",
      fotos:[],
      filtro:'',
      mensagem:''
    }
  },
  created(){
    this.service = new FotoService(this.$resource);
    
    this.service.lista().then(fotos => this.fotos = fotos,err => {
      this.mensagem = err.message
      }
      );
    
  },
  computed: {
    fotosComFiltro(){
      if(this.filtro) {
        let exp = new RegExp(this.filtro.trim(), 'i');
        return this.fotos.filter(foto => exp.test(foto.titulo));
      }else {
        return this.fotos
      }
    }
  },
  methods:{

      remove(foto){

        this.service.apagar(foto).then(() => { 
            this.mensagem = "foto removida com sucesso"
            let indice = this.fotos.indexOf(foto);
            this.fotos.splice(indice,1);
            },err => {
          console.log(err)
          this.mensagem("NÃ£o foi possivel remover a foto");
          } );
        

          
      }

  }
}
</script>

